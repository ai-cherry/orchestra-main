    def save_env(self):
        """Save configuration to .env file."""
        with open(self.env_file, "w") as f:
            f.write("# Cherry AI Environment Configuration\n")
            f.write(f"# Generated by Setup Wizard on {time.strftime('%Y-%m-%d %H:%M:%S')}\n\n")

            # Group by category
            categories = {
                "Environment": ["ENVIRONMENT"],
                "External Services": [
                    "WEAVIATE_URL",
                    "WEAVIATE_API_KEY",
                ],
                "Local Services": ["REDIS_HOST", "REDIS_PORT"],
                "LLM APIs": [
                    "OPENROUTER_API_KEY",
                    "OPENAI_API_KEY",
                    "ANTHROPIC_API_KEY",
                    "PORTKEY_API_KEY",
                ],
                "Application": ["SITE_URL", "SITE_TITLE"],
            }

            for category, keys in categories.items():
                f.write(f"# {category}\n")
                for key in keys:
                    if key in self.config:
                        f.write(f"{key}={self.config[key]}\n")
                f.write("\n")

